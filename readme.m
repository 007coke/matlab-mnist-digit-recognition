# MATLAB手写数字识别系统（LeNet + SVM双模型）
## 项目简介
本项目基于MATLAB R2016开发，实现手写数字识别功能，同时提供LeNet卷积神经网络、SVM支持向量机两种识别方案，配套GUI可视化交互界面，可导入本地手写图片完成识别。针对1与8、4与9等易混淆数字增加几何特征校验逻辑，优化识别精度。

## 文件目录说明
本仓库已上传全部项目配套资源，包含样本文件夹、训练代码.
### 1. 样本文件夹
1. `num/`
存放0~9自定义手写数字图片，作为自定义测试样本，用于本地图片识别测试。
2. `std_num/`
存放标准化预处理后的0~9标准数字样本，用于模型训练与识别效果对照。

### 2. 训练源码
`train_lenet.m`
LeNet卷积网络训练主程序，执行后完成CNN网络训练，输出可用于预测的网络权重，可单独存放、单独运行；脚本内部集成SVM完整训练逻辑。

### 3. 预测程序压缩包 `predict.zip`
压缩包内包含识别配套三件套，**三个文件必须放在同一个文件夹内运行，不可拆分**：
- `predict_lenet.m`：整套识别程序唯一运行入口，必须点开此文件启动识别，程序会自动调用另外两个配套脚本
- `recognize_digit.m`：数字预处理与识别总逻辑，统一完成图片灰度转换、二值化、裁剪、降噪、笔画增强，同时兼容LeNet、SVM两种模型输出识别结果
- `open_img_callback.m`：GUI界面图片加载回调函数，负责读取本地图片并送入识别主逻辑

### 4. SVM模型分卷压缩包说明
`svm_mnist_model.mat`原始文件体积超200MB，平台单文件上传限制25MB，因此拆分为单卷24MB的7z分卷包（`svm_mnist_model.7z.001`、`svm_mnist_model.7z.002`……）。

## 运行环境要求
- MATLAB R2016 及以上版本
- 必备工具箱：
  1. Deep Learning Toolbox（LeNet网络训练）
  2. Image Processing Toolbox（图片预处理）
  3. Statistics and Machine Learning Toolbox（SVM分类模型）


### 前置操作
1. 下载仓库内 `predict.zip`，完整解压到独立文件夹，保证内部三个`.m`文件在同一目录，不要拆分存放；
2. 由于生成的`svm_mnist_model.mat` 模型文件 ，过大无法上传，需要自行生成，详细步骤如下
3. 压缩包需全部解压，并且处于根目录中。

---
###本地运行代码，自行训练生成SVM模型
1. 将 `train_lenet.m` 训练脚本放在根目录下与`mnist_test `mnist_train一起；
2. 使用MATLAB打开 `train_lenet.m`，点击运行；
3. 程序自动读取样本数据集，完成LeNet卷积网络训练与SVM分类模型训练；
4. 训练结束后，项目根目录自动生成完整 `svm_mnist_model.mat`；
5. 将该模型文件放置在，进入解压后的predict文件夹，打开并运行 `predict_lenet.m`；
6. 通过GUI界面加载`num`文件夹内手写图片，程序自动完成识别，输出LeNet、SVM双模型识别结果。

---
## 核心功能特点
1. 双模型对比识别：卷积神经网络CNN + 传统机器学习SVM，方便识别效果对比；
2. 完整图片预处理流水线：白底黑字转换、噪声去除、笔画加粗、数字定位裁剪；
3. GUI可视化操作界面，支持本地图片上传识别；
4. 内置几何特征判别规则，修正1/8、4/9等高混淆数字识别错误。

## 重要注意事项
1. predict压缩包解压后的 `predict_lenet.m`、`recognize_digit.m`、`open_img_callback.m` 三个脚本必须同目录存放，拆分后运行会出现函数调用报错；
2. 运行整套识别程序时，必须打开并执行 `predict_lenet.m`，该文件为程序唯一主入口；
3. 用于识别的自定义图片建议白底、仅包含单个数字，无多余杂物，识别准确率更高；
4. MATLAB版本低于R2016会存在函数不兼容问题，推荐使用R2016及以上版本。
